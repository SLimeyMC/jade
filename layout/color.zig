pub const LinearRgb = struct { r: i32, g: i32, b: i32 };
pub const SrgbLayout = struct { r: u8, g: u8, b: u8 };
pub const OkLabLayout = struct { l: i32, a: i32, b: i32 };

pub fn Color(
	comptime Layout: type,
	comptime toLinearFn: fn (Layout) LinearRgb,
	comptime fromLinearFn: fn (LinearRgb) Layout,
) type {
	return struct {
		const Self = @This();

		value: Layout,

		pub fn toLinear(self: Self) LinearRgb {
			return toLinearFn(self.value);
		}

		pub fn fromLinear(linear: LinearRgb) Self {
			return .{ .value = fromLinearFn(linear) };
		}

		pub fn to(self: Self, comptime Target: type) Target {
			return Target.fromLinear(self.toLinear());
		}
	};
}

