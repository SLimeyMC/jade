fn divRound64(a: i64, b: i64) i64 {
	return if ((a ^ b) < 0) @divTrunc(a - @divTrunc(b, 2), b) else @divTrunc(a + @divTrunc(b, 2), b);
}

fn cbrt01Int(x_in: i32) i32 {
	if (x_in <= 0) return 0;
	if (x_in >= K) return K;
	const x: i64 = x_in;
	var u: i64 = @divTrunc(x * @divTrunc(x * @divTrunc(x - 144107, K) + 132114, K) + 14379, 1);
	for (0..2) |_| {
		const uuu = u * u * u;
		const den = x + @divTrunc(2 * uuu + @divTrunc(K2, 2), K2);
		u = @divTrunc(u * (2 * x + @divTrunc(uuu + @divTrunc(K2, 2), K2)) + @divTrunc(den, 2), den);
	}
	return @intCast(u);
}

fn linearIntToSrgbU8(x: i32) u8 {
	if (x <= 0) return 0;
	if (x >= K) return 0xff;
	const xP = x * P;
	const i = @divTrunc(xP, K);
	const m = @mod(xP, K);
	const y0: i32 = linear2srgb[@intCast(i)];
	const y1: i32 = linear2srgb[@intCast(i + 1)];
	return @intCast(@divTrunc(m * (y1 - y0) + @divTrunc(K, 2), K) + y0);
}

fn srgbToLinear(srgb: SrgbLayout) LinearRgb {
	return .{
		.r = srgb2linear[srgb.r],
		.g = srgb2linear[srgb.g],
		.b = srgb2linear[srgb.b],
	};
}

fn linearToSrgb(lin: LinearRgb) SrgbLayout {
	return .{
		.r = linearIntToSrgbU8(lin.r),
		.g = linearIntToSrgbU8(lin.g),
		.b = linearIntToSrgbU8(lin.b),
	};
}

fn oklabToLinear(lab: OkLabLayout) LinearRgb {
	const l_: i64 = lab.l + divRound64(25974 * lab.a, K) + divRound64( 14143 * lab.b, K);
	const m_: i64 = lab.l + divRound64(-6918 * lab.a, K) + divRound64( -4185 * lab.b, K);
	const s_: i64 = lab.l + divRound64(-5864 * lab.a, K) + divRound64(-84638 * lab.b, K);

	return .{
		.r = @intCast(@divTrunc( 267169 * @divTrunc(l_*l_*l_, K2) - 216771 * @divTrunc(m_*m_*m_, K2) +  15137 * @divTrunc(s_*s_*s_, K2) + @divTrunc(K, 2), K)),
		.g = @intCast(@divTrunc( -83127 * @divTrunc(l_*l_*l_, K2) + 171030 * @divTrunc(m_*m_*m_, K2) -  22368 * @divTrunc(s_*s_*s_, K2) + @divTrunc(K, 2), K)),
		.b = @intCast(@divTrunc(   -275 * @divTrunc(l_*l_*l_, K2) -  46099 * @divTrunc(m_*m_*m_, K2) + 111909 * @divTrunc(s_*s_*s_, K2) + @divTrunc(K, 2), K)),
	};
}

fn linearToOklab(lin: LinearRgb) OkLabLayout {
	const l = @divTrunc(27015 * lin.r + 35149 * lin.g +  3372 * lin.b + @divTrunc(K, 2), K);
	const m = @divTrunc(13887 * lin.r + 44610 * lin.g +  7038 * lin.b + @divTrunc(K, 2), K);
	const s = @divTrunc( 5787 * lin.r + 18462 * lin.g + 41286 * lin.b + @divTrunc(K, 2), K);

	const l_ = cbrt01Int(l);
	const m_ = cbrt01Int(m);
	const s_ = cbrt01Int(s);

	return .{
		.l = @intCast(divRound64( 13792 * l_ +  52010 * m_ -   267 * s_, K)),
		.a = @intCast(divRound64(129628 * l_ - 159158 * m_ + 29530 * s_, K)),
		.b = @intCast(divRound64(  1698 * l_ +  51299 * m_ - 52997 * s_, K)),
	};
}

